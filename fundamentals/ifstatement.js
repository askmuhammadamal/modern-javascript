const mark = '50'

//abstract equality comparasion
if (mark == 50) {
    console.log('the mark is 50')
} else {
    console.log('the mark is not 50')
}

//strictly equality comparasion
if (mark === 50) {
    console.log('the mark is 50')
} else {
    console.log('the mark is not 50')
}

if (mark !== 50) {
    console.log('the mark is 50')
} else {
    console.log('the mark is not 50')
}

//greater than or equal
if (mark >= 60) {
    console.log('the mark is 50')
} else {
    console.log('the mark is not 50')
}